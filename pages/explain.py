# 1. 언어 사용과 심리의 관계
st.subheader("1. 언어 사용과 인간 심리의 상관관계")
st.markdown("""
인간이 사용하는 언어는 단순한 의사소통 도구를 넘어 **뇌의 인지적 처리 상태와 감정적 과부하**를 직접적으로 반영합니다.

* **인지적 모호성 어휘 (Ambiguity Words):**  
  인간은 해결하기 어려운 상황이나 불확실성에 직면할 때 `어떻게`, `모르겠다`, `막막하다`와 같은 모호성 어휘를 무의식적으로 빈번하게 사용합니다. 이는 뇌의 전두엽이 상황 판단을 확정 짓지 못하고 헤매는 상태를 의미합니다.
* **인과 관계 어휘 (Causal Words):**  
  `~때문에`, `결국`, `이유는`과 같은 단어는 뇌가 스트레스 상황의 원인을 분석하고 통제력을 회복하려는 **'인지적 재구성 시도'**에서 비롯됩니다.
* **자아 대명사 (First-person Pronouns):**  
  `나`, `내` 등의 1인칭 대명사는 스트레스나 인지적 피로도가 증가할 때 외부 환경보다 자신의 내면 정서로 주의(Attention)가 쏠리면서 출현 비중이 크게 늘어납니다.
""")

